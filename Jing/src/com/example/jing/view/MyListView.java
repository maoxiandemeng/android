	public void notifyDataSetChanged(BaseAdapter adapter) {
		if (onEmptyListener != null) {
			onEmptyListener.onRemoveEmpty();
		}
		if (adapter != null && adapter.getCount() == 0) {
			if (onEmptyListener != null) {
				onEmptyListener.onAddEmpty();
			}
		}
		adapter.notifyDataSetChanged();
	}

